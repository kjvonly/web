export class BibleNavigationService {
	chapterList: string[];
	bookNames: any;
	constructor() {
		bibleDB.ready.then((val) => {
			if (!val) {
				return;
			}
			bibleDB.getValue('chapters', 'booknames').then((data) => {
				this.bookNames = data;
			});
		});
		this.chapterList = chapters;
	}

	next(currentKey: string): string {
		let ci = this.chapterList.indexOf(currentKey);
		if (ci + 1 > this.chapterList.length) {
			return this.chapterList[0];
		} else {
			return this.chapterList[ci + 1];
		}
	}

	previous(currentKey: string): string {
		let ci = this.chapterList.indexOf(currentKey);
		if (ci - 1 < 0) {
			return this.chapterList[this.chapterList.length - 1];
		} else {
			return this.chapterList[ci - 1];
		}
	}

	goto(shortBook: string, chapter: string) {
		for (const [key, value] of Object.entries(this.bookNames['shortNames'])) {
			if (shortBook.toLowerCase() === value.toLowerCase()) {
				return key + '_' + chapter;
			}
		}
	}
}

export let bibleNavigationService = new BibleNavigationService();
